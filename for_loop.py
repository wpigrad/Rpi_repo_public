# title: for_loop.py

# using different types of for loops

print('\n*****\n')

for number in range(3,10,2):
	print(number)

print('\n*****\n')

for number in range(4):
	print(number)

print('\n*****\n')

letters = ['a','b','c']
for letter in letters:
	print(letter)

print('\n*****\n')

names = ['Tim','Sue','Joe']
for number,name in enumerate(names):
	print(number,name)

print('\n*****\n')
